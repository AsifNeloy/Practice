#include<bits/stdc++.h>
using namespace std;

int main(){
          int input[4][4],i=1;
          for(int row=0;row<4;row++){
                    i=1;
                    for(int col=0;col<=row++;col++){
                              input[row][col]=i;
                              i++;
                    }
          }
          for(int row=0;row<4;row++){
                    for(int col=0;col<4;col++){
                              cout<<input[row][col]<<" ";
                    }
          }

return 0;}
